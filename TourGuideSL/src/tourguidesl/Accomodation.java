    private void entrbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrbtnActionPerformed
        String name = emailfield.getText();
        String email = namefield.getText();
        java.sql.Date indate = new java.sql.Date(chkinDate.getDate().getTime());
        java.sql.Date outdate = new java.sql.Date(chkoutDate.getDate().getTime());
        try {
            DBConnection dbcon = new DBConnection();
            if (!"".equals(name) && !"".equals(indate) && !"".equals(outdate)) {
                String Query = "INSERT into booking (name,email,indate,outdate) values('" + name + "','" + email + "','" + indate + "','" + outdate + "');";

                dbcon.ExecuteQuery(Query);
                namefield.setText("");
                emailfield.setText("");
            } else {
                JOptionPane.showMessageDialog(f, "Please check for null fields, cannot enter null values to database");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ClientRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ClientRegister.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_entrbtnActionPerformed

