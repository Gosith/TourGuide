    private void loadrecords() {
        DefaultTableModel tblmdel = (DefaultTableModel) recordtbl.getModel();
        tblmdel.setRowCount(0);
        try {
            String query_one = "SELECT name,email from client";
            String query_two = "SELECT indate,outdate from booking";
            DBConnection dbcon = new DBConnection();
            ResultSet rs1 = dbcon.GetQuery(query_one);
            ResultSet rs2 = dbcon.GetQuery(query_two);

            try {
                while (rs1.next() && rs2.next()) {
                    String name = rs1.getString("name");
                    String email = rs1.getString("email");
                    String indate = rs2.getString("indate");
                    String outdate = rs2.getString("outdate");
                    String dateData[] = {name, email, indate, outdate};
                    tblmdel.addRow(dateData);
                }
            } catch (Exception e) {
                System.out.println(e);
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ClientRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ClientRegister.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void loadrecord() {
        int row = recordtbl.getSelectedRow();
        namefield.setText((String) recordtbl.getModel().getValueAt(row, 0));
        emailfield.setText((String) recordtbl.getModel().getValueAt(row, 1));
        namefield.setEditable(false);
        emailfield.setEditable(false);
    }

